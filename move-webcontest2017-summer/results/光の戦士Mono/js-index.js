//配列ストーリテキストを1文字ずつ表示
function pptext(count) {
	//文章の長さを条件にして、substringメソッドで表示
	if (storyTextList[count].length >= i) {
		document.getElementById("viewArea").innerHTML = storyTextList[count].substring(0, i);
		//表示速度をここで設定
		timer1 = setTimeout("pptext(count)", 80);
		i = i + 1;
	}
	else {
		i = 0;
		clearTimeout(timer1);
	}
}

function hyoji(count) {
	if (count == 5) {
		document.getElementById("option1").style.display = "block";
		document.getElementById("gazo").style.display = "none";
		document.getElementById("gazo-bg").style.display = "none";
	} else if (count == 1) {
		document.getElementById("intro").style.display = "none";
		document.getElementById("gazo").style.display = "none";
		onRadioButtonChange();
	} else if (count == 11) {
		document.getElementById("gazo").style.display = "block";
		document.getElementById("intro").style.display = "none";
	} else if (count == 12) {
		document.getElementById("gazo").style.display = "block";
		document.getElementById("intro").style.display = "none";
	} else if (count == 23) {
		document.getElementById("gazo").style.display = "block";
		document.getElementById("intro").style.display = "none";
	} else if (count == 0) {
		
		target = document.getElementById("output");
		target.innerHTML = "自分を信じる<br/>";
		radiobtn1.checked = false;
		radiobtn2.checked = false;
		
	} else {
		document.getElementById("option1").style.display = "none";
		document.getElementById("gazo").style.display = "none";
		document.getElementById("gazo-bg").style.display = "block";
		document.getElementById("intro").style.display = "none";
		document.getElementById("viewArea").style.display = "block";
	}
}

function getNumber() {
	if (count == maxText-1) {//ここの条件は行列の最大値にして文章が最初に戻る
		count = 0;
	} else {
		count++;
	}
	//表示させるかどうかの判断
	hyoji(count);
	//pptextでストーリー文章の行列を1文字ずつ表示させる
	pptext(count);
	//gazoでストーリーの人物画像を表示させる(png)
	document.getElementById("gazo").src = img[count].src;
	//gazo-bgでストーリーの背景画像を表示させる(jpeg)
	document.getElementById("gazo-bg").src = imgbg[count].src;
}

